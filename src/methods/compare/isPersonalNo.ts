/********************************************************************************************************************
 * 주민등록번호 형식인지 확인하는 함수
 * @param v 확인할 값
 * @returns 주민등록번호 형식이면 true, 그렇지 않으면 false 반환
 * ******************************************************************************************************************/

export function isPersonalNo(v: string): boolean {
  return /(([0-9]{6})([0-9]{7}))|(([0-9]{6})-([0-9]{7}))/.test(v);
}

export default isPersonalNo;
