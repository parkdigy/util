/********************************************************************************************************************
 * 값이 비어있는 경우 대체 값을 반환하는 함수
 * @param v 확인할 값
 * @param v2 대체 값
 * @returns 최종 값
 * ******************************************************************************************************************/

import empty from './empty';

export function ifEmpty<TV, TNV>(v: TV, v2: TNV): TV | TNV {
  return empty(v) ? v2 : v;
}

export default ifEmpty;
