/********************************************************************************************************************
 * 이메일 형식인지 확인하는 함수
 * @param v 확인할 값
 * @returns 이메일 형식이면 true, 그렇지 않으면 false 반환
 * ******************************************************************************************************************/

export function isEmail(v: string): boolean {
  return new RegExp(
    /^(([^<>()[\].,;:\s@"]+(\.[^<>()[\].,;:\s@"]+)*)|(".+"))@(([^<>()[\].,;:\s@"]+\.)+[^<>()[\].,;:\s@"]{2,})$/,
    'g'
  ).test(v);
}

export default isEmail;
