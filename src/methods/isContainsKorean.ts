/********************************************************************************************************************
 * 한글이 포함되어 있는지 확인하는 함수
 * ******************************************************************************************************************/

export function isContainsKorean(text: string): boolean {
  const korean = /[ㄱ-ㅎ|ㅏ-ㅣ|가-힣]/;
  return korean.test(text);
}

export default isContainsKorean;
